public class User {

    String userName;
    int userId;

    public String getName() {
        return this.userName;
    }

    public void setName(String name) {
        this.userName = name;
    }

    public int getId() {
        return this.userId;
    }

    public void setId(int id) {
        this.userId = id;
    }
}
